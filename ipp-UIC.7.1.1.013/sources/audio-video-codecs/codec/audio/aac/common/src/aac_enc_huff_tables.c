AACStatus BuildHuffmanTables(IppsVLCEncodeSpec_32s** pTables,
                             Ipp32s *sizeAll)
{
  Ipp32s i, mSize;
#if !defined(ANDROID)
  Ipp32s size[12];
#else
  static Ipp32u size[12];
#endif

  mSize = 0;
  for (i = 0; i < 12; i++) {
    if (ippsVLCEncodeGetSize_32s(vlcEncBooks[i],
                                 vlcEncTableSizes[i],
                                 &size[i]) != ippStsOk) {
      return AAC_ALLOC;
    }
    mSize += size[i];
  }
  
  *sizeAll = mSize;

  if (pTables) {
    for (i = 1; i < 12; i++) {
      pTables[i] = (IppsVLCEncodeSpec_32s*)((Ipp8u*)pTables[i-1] + size[i-1]);
    }

    for (i = 0; i < 12; i++) {
      if (ippsVLCEncodeInit_32s(vlcEncBooks[i],
                                vlcEncTableSizes[i],
                                pTables[i]) != ippStsOk) {
        return AAC_ALLOC;
      }
    }
  }
  return AAC_OK;
}

